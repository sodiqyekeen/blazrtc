namespace BlazRTC;

/// <summary>
/// Represents the kind of media device.
/// </summary>
public enum MediaDeviceKind
{

    /// <summary>
    /// Represents an unknown media device kind.
    /// </summary>
    Unknown,

    /// <summary>
    /// Represents an audio input device.
    /// </summary>
    AudioInput,

    /// <summary>
    /// Represents an audio output device.
    /// </summary>
    AudioOutput,

    /// <summary>
    /// Represents a video input device.
    /// </summary>
    VideoInput
}
